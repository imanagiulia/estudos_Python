codigo = 120
nome = 'ana aguiar'
salario = 2500.00
ativo = True

print("Código: %d "% codigo)
print("Nome: %s"% nome)
print("salario: %.2f"% salario)
print("Ativo: %r"% ativo)

# %d/%i = inteiro
# %f = float
# %ld = long int
# %r = bool
# %c = caracter
# %s = string
# %e/%E = número exponencial
# %o = int em formato octal
# %x/%X = int em formato hexadecimal